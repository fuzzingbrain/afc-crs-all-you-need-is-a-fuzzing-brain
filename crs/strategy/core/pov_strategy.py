    def has_successful_pov(self) -> bool:
        """Check if successful POV already exists"""
        if not os.path.exists(self.config.pov_success_dir):
            return False
        pov_files = [f for f in os.listdir(self.config.pov_success_dir) if f.startswith("pov_metadata_")]
        return len(pov_files) > 0
